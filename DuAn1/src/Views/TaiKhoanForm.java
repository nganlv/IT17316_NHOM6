public void loadTable(){
        List<QuanLyTaiKhoan> listCV = tkService.getView();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        model.setColumnIdentifiers(new String[]{"ID","Mã ", "Tên ","mat khau"});
        for (QuanLyTaiKhoan cv : listCV) {
            model.addRow(new Object[]{cv.getId(),cv.getMa(), cv.getTen(),cv.getMatkhau()});
        }
        tblTK.setModel(model);
    }
   public void loadTextField(){
        int index = tblTK.getSelectedRow();
         txtId.setText(tblTK.getValueAt(index, 0).toString());
        txtMa.setText(tblTK.getValueAt(index, 1).toString());
        txtTen.setText(tblTK.getValueAt(index, 2).toString());
        txtMK.setText(tblTK.getValueAt(index, 3).toString());
    }
    public TaiKhoan getData(){
       TaiKhoan cv = new TaiKhoan();
        cv.setMa(txtMa.getText().trim());
        cv.setTen(txtTen.getText().trim());
        cv.setMatkhau(txtMK.getText().trim());
        return cv;
    }
    public  boolean checkValidate(){
        if(txtMa.getText().isBlank()||txtTen.getText().isBlank()||txtMK.getText().isBlank()){
            JOptionPane.showMessageDialog(rootPane,"du lieu ko duoc de trong");
            return false;
        }
     
        if(txtMK.getText().trim().length() > 8){
            JOptionPane.showMessageDialog(this, "mat khau khong qua 8 ky tu");
            return false;
        }
       return true;
    
    }
