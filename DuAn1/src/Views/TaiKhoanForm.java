    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(checkValidate()){
            if(tkService.checkMa(txtMa.getText().trim())){
                JOptionPane.showMessageDialog(this, "Mã tai khoan đã tồn tại");
            }else{
                JOptionPane.showMessageDialog(this, tkService.addCVs(getData()));
                loadTable();
            }

        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if(checkValidate()){
            if(!tkService.checkMa(txtMa.getText().trim())){
                JOptionPane.showMessageDialog(this, "Mã tai khoan không tồn tại");
            }else{
                JOptionPane.showMessageDialog(this, tkService.updateCV(getData()));
                loadTable();
            }

        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if(tblTK.getSelectedRow() < 0){
            JOptionPane.showConfirmDialog(this, "Mời chọn 1 dòng");
        }else{
            int chon = JOptionPane.showConfirmDialog(this, "Bạn chắn chắn muốn xóa", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if(chon == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(this, tkService.deleteCV(txtMa.getText().trim()));
                loadTable();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

